## OPERADORES ARITMETIC
produto_1 = 10
produto_2 = 20

print(f"SOMA:  {produto_1} + {produto_2}")  ## soma
print(f"SUBTRAÇÃO:  {produto_1} - {produto_2}")  ## subtração  
print(f"MULTIPLICAÇÃO:  {produto_1} * {produto_2}")  ## multiplicação
print(f"DIVISÃO: {produto_1} / {produto_2}")  ## divisão
print(f"DIVISÃO INTEIRA:  {produto_1} // {produto_2}")  ## divisão inteira
print(f"RESTO DA DIVISÃO:  {produto_1} % {produto_2}")  ## resto da divisão
print(f"POTENCIA:  {produto_1} ** 2")  ## potência

print(produto_1 + produto_2 )  ## ordem de precedência